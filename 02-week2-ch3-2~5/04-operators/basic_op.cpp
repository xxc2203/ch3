#include <iostream>
using namespace std;

int main()
{
	int x = 6;
	x += 3;
	cout<<"6 + 4 = " << (6 + 4) << endl;
	cout<<"6 - 4 = " << (6 - 4) << endl;
	cout<<"6 * 4 = " << (6 * 4) << endl;
	cout<<"6 / 4 = " << (6 / 4) << endl;
	cout<<"6 % 4 = " << (6 % 4) << endl;	
	cout<<"float x = 6, x += 3; --> x = " << x << endl;
	system("Pause");
}
